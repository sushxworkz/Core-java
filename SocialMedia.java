class SocialMedia{

	int socialMediaId;
	String name;
	String profileName;
	String email;
	
	public SocialMedia(){
		System.out.println("Create the object");
	}
	
	public SocialMedia(int socialMediaId,String name,String profileName,String email){
	
			this.	socialMediaId=socialMediaId;
			this.	name=name;
			this.	profileName=profileName;
			this.	email=email;
	}
	
				
}